#include <bits/stdc++.h>
 
using namespace std;

int main() {

  // Declare essential variables.
  int i, n;

  // Taking input from user.
  cin >> n;
  
  // calculate remaining .
  for (i = i; i <= 10000; i++) {
    if (i%n == 2) {
      cout << i << endl;
    }
  }

  return 0;
}
