#include <iostream>
 
using namespace std;
 
int main() {
    
    // Declare essential variables.
    int a, b, c, d, diff;
    
    // Taking input from user.
    cin >> a >> b >> c >> d;
    
    // calculate difference and print.
    diff = a*b - c*d;
    cout << "DIFERENCA = " << diff << endl;
    
    return 0;
}