#include<bits/stdc++.h>
using namespace std;
int main() {

  // Declare essential variables.
  int t, r1, r2, radius;

  // Take the number of test cases.
  cin >> t;

  while(t--) {
    // Taking input while t is true;
    cin >> r1 >> r2;

    // Calculate and print output.
    radius = r1 + r2;
    cout << radius << endl;
  }  

  return 0;
}

